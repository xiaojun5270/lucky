import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { resolve } from 'node:path';

const root = resolve(import.meta.dirname, '..');
const sourcePath = resolve(root, 'docs', 'Lucky_API_Endpoints.json');
const outputPath = resolve(root, 'src', 'api', 'lucky-endpoints.generated.ts');
const checkOnly = process.argv.includes('--check');

const moduleLabels = {
  base: '基础资源', baseconfigure: '基础配置', cloudflared: 'Cloudflared', coraza: 'Coraza / WAF',
  cron: '计划任务', ddns: '动态域名 DDNS', ddnstasklist: 'DDNS 任务列表', dlnaservice: 'DLNA 服务',
  docker: 'Docker 管理', frp: 'FRP', ftpserver: 'FTP 服务', iconlib: '图标库', info: '系统信息',
  ipdb: 'IP 数据库', ipfliter: 'IP 过滤', ipregtest: 'IP 规则测试', login: '登录', logout: '退出登录',
  logs: '全局日志', lucky: 'Lucky 服务', modules: '模块状态', netinterfaces: '网络接口', oauth: 'OAuth',
  portforward: '端口转发', portforwards: '端口转发列表', portforwards_lite: '端口转发精简列表',
  rclone: 'Rclone', reboot_program: '重启程序', restoreconfigureconfirm: '恢复配置', ssl: 'SSL / TLS 证书',
  status: '运行状态', storagemanagement: '存储管理', stun: 'STUN', stunrule: 'STUN 规则',
  stunrulelist: 'STUN 规则列表', stunrulelist_lite: 'STUN 精简列表', third: '第三方服务',
  thirdPartyAuthManager: '第三方认证', twofapassword: '双因素认证', update: '程序更新', v2l: 'V2L',
  webdav: 'WebDAV', webservice: '反向代理 / Web 服务', webterminal: 'WebTerminal', wol: '网络唤醒',
};

const raw = JSON.parse(await readFile(sourcePath, 'utf8'));
if (!Array.isArray(raw) || raw.length === 0) throw new Error('Endpoint source must be a non-empty array.');
const seen = new Map();
const endpoints = raw.map((item, index) => {
  const methods = String(item.methods).replaceAll('*', '').split('/').filter(Boolean);
  if (!item.path || !item.module || methods.length === 0) throw new Error(`Invalid endpoint row ${index + 1}.`);
  if (methods.some((method) => !['GET', 'POST', 'PUT', 'DELETE', 'PATCH'].includes(method))) {
    throw new Error(`Unsupported HTTP method in row ${index + 1}: ${item.methods}`);
  }
  const baseId = `${item.module}-${String(index + 1).padStart(3, '0')}`;
  const idCount = (seen.get(baseId) ?? 0) + 1;
  seen.set(baseId, idCount);
  const pathVariables = [...String(item.path).matchAll(/\$\{([^}]+)\}/g)].map((match) => match[1]);
  return {
    id: idCount === 1 ? baseId : `${baseId}-${idCount}`,
    path: item.path,
    methods,
    module: item.module,
    source: item.source ?? '',
    notes: item.notes ?? '',
    requiresSuffix: String(item.path).endsWith('/') || pathVariables.length > 0,
    pathVariables,
  };
});

const modules = [...new Set(endpoints.map((item) => item.module))].map((key) => {
  const items = endpoints.filter((item) => item.module === key);
  return {
    key,
    label: moduleLabels[key] ?? key,
    endpointCount: items.length,
    methodCount: items.reduce((total, item) => total + item.methods.length, 0),
  };
}).sort((left, right) => left.label.localeCompare(right.label, 'zh-CN'));

const content = `// Generated by npm run generate:endpoints. Do not edit manually.\n` +
  `import type { LuckyEndpointDefinition, LuckyModuleDefinition } from '@/src/types/lucky';\n\n` +
  `export const LUCKY_ENDPOINTS = ${JSON.stringify(endpoints, null, 2)} as const satisfies readonly LuckyEndpointDefinition[];\n\n` +
  `export const LUCKY_MODULES = ${JSON.stringify(modules, null, 2)} as const satisfies readonly LuckyModuleDefinition[];\n`;

const methodCount = endpoints.reduce((sum, item) => sum + item.methods.length, 0);
if (new Set(endpoints.map((item) => item.id)).size !== endpoints.length) throw new Error('Generated endpoint IDs are not unique.');
if (checkOnly) {
  const current = await readFile(outputPath, 'utf8');
  if (current !== content) throw new Error('Generated endpoint registry is stale. Run npm run generate:endpoints.');
  console.log(`Endpoint registry OK: ${endpoints.length} endpoints, ${methodCount} methods, ${modules.length} modules.`);
} else {
  await mkdir(resolve(root, 'src', 'api'), { recursive: true });
  await writeFile(outputPath, content, 'utf8');
  console.log(`Generated ${endpoints.length} endpoints, ${methodCount} methods, ${modules.length} modules.`);
}
